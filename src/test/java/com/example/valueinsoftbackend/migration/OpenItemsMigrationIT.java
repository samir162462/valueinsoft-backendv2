    /** Phase 8.3: concurrent exposure checks serialize on the client row. */
    @Test
    void concurrentExposureChecksCannotOversubscribeClientLimit() throws Exception {
        org.junit.jupiter.api.Assumptions.assumeTrue(Boolean.getBoolean("openitems.soak.enabled"));
        int clientId;
        try (Connection c = connect()) {
            clientId = insertClient(c);
            exec(c, "UPDATE " + SCHEMA + ".\"Client\" SET credit_limit=100 WHERE c_id=" + clientId);
        }
        DriverManagerDataSource dataSource = new DriverManagerDataSource(
                POSTGRES.getJdbcUrl(), POSTGRES.getUsername(), POSTGRES.getPassword());
        DbArOpenItem repository = new DbArOpenItem(new NamedParameterJdbcTemplate(dataSource));
        TransactionTemplate transaction = new TransactionTemplate(new DataSourceTransactionManager(dataSource));
        CyclicBarrier start = new CyclicBarrier(20);
        AtomicInteger accepted = new AtomicInteger();

        try (ExecutorService pool = Executors.newVirtualThreadPerTaskExecutor()) {
            List<Future<?>> futures = new ArrayList<>();
            for (int i = 0; i < 20; i++) {
                int sale = i;
                futures.add(pool.submit(() -> {
                    start.await();
                    transaction.executeWithoutResult(ignored -> {
                        repository.lockClientCredit(COMPANY_ID, clientId);
                        java.math.BigDecimal exposure = repository.sumClientOpenExposure(
                                COMPANY_ID, clientId, "EGP");
                        if (exposure.add(java.math.BigDecimal.TEN).compareTo(new java.math.BigDecimal("100")) <= 0) {
                            repository.createOpenItem(COMPANY_ID, BRANCH_ID, clientId, "POS_ORDER",
                                    800000L + sale, "P8-SALE-" + sale, java.time.LocalDateTime.now(),
                                    java.time.LocalDateTime.now(), "EGP", java.math.BigDecimal.TEN,
                                    "phase8-credit-sale-" + clientId + "-" + sale, "phase8-soak");
                            accepted.incrementAndGet();
                        }
                    });
                    return null;
                }));
            }
            for (Future<?> future : futures) future.get();
        }

        assertEquals(10, accepted.get(), "Only ten 10-unit sales may fit a 100-unit limit");
        assertEquals(0, new java.math.BigDecimal("100").compareTo(
                repository.sumClientOpenExposure(COMPANY_ID, clientId, "EGP")));
    }

