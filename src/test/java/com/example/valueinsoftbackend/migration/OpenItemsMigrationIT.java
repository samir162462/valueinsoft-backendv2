    /**
     * Phase 8.3 opt-in soak: 50 independent receipts contend for ten FIFO items.
     * Run with -Dopenitems.soak.enabled=true on a Docker-enabled host.
     */
    @Test
    void fiftyConcurrentAllocationsAcrossTenItemsHaveNoDeadlocks() throws Exception {
        org.junit.jupiter.api.Assumptions.assumeTrue(Boolean.getBoolean("openitems.soak.enabled"));
        int clientId;
        int[] receiptIds = new int[50];
        try (Connection c = connect()) {
            clientId = insertClient(c);
            for (int i = 0; i < 10; i++) insertArOpenItem(c, clientId, "10");
            for (int i = 0; i < receiptIds.length; i++) receiptIds[i] = insertReceipt(c, clientId, "2");
        }

        DriverManagerDataSource dataSource = new DriverManagerDataSource(
                POSTGRES.getJdbcUrl(), POSTGRES.getUsername(), POSTGRES.getPassword());
        DbArOpenItem repository = new DbArOpenItem(new NamedParameterJdbcTemplate(dataSource));
        ArOpenItemService service = new ArOpenItemService(repository,
                org.mockito.Mockito.mock(com.example.valueinsoftbackend.Service.finance.FinanceOperationalPostingService.class));
        TransactionTemplate transaction = new TransactionTemplate(new DataSourceTransactionManager(dataSource));
        long deadlocksBefore = databaseDeadlocks();
        CyclicBarrier start = new CyclicBarrier(receiptIds.length);
        AtomicInteger failures = new AtomicInteger();

        try (ExecutorService pool = Executors.newVirtualThreadPerTaskExecutor()) {
            List<Future<?>> futures = new ArrayList<>();
            for (int receiptId : receiptIds) {
                futures.add(pool.submit(() -> {
                    start.await();
                    try {
                        transaction.executeWithoutResult(ignored -> service.allocateReceipt(
                                COMPANY_ID, BRANCH_ID, clientId, receiptId,
                                new OpenItemsWriteModels.AllocationCommand("EGP", key(), List.of()), "phase8-soak"));
                    } catch (RuntimeException exception) {
                        failures.incrementAndGet();
                    }
                    return null;
                }));
            }
            for (Future<?> future : futures) future.get();
        }

        assertEquals(0, failures.get(), "Every serialized FIFO allocation must succeed");
        assertEquals(deadlocksBefore, databaseDeadlocks(), "The soak must not increase PostgreSQL deadlocks");
        try (Connection c = connect(); Statement s = c.createStatement(); ResultSet rs = s.executeQuery(
                "SELECT SUM(settled_amount), SUM(remaining_amount) FROM " + SCHEMA
                        + ".ar_open_item WHERE client_id=" + clientId)) {
            assertTrue(rs.next());
            assertEquals(0, new java.math.BigDecimal("100.0000").compareTo(rs.getBigDecimal(1)));
            assertEquals(0, java.math.BigDecimal.ZERO.compareTo(rs.getBigDecimal(2)));
        }
    }

