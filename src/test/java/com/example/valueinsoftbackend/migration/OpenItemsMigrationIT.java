    @Test
    void allocationEnforcesCapsAndCoherence() throws Exception {
        try (Connection c = connect()) {
            int clientId = insertClient(c);
            int otherClient = insertClient(c);
            int receiptId = insertReceipt(c, clientId, "100");
            long itemA = insertArOpenItem(c, clientId, "60");
            long itemB = insertArOpenItem(c, clientId, "60");
            long foreignItem = insertArOpenItem(c, otherClient, "60");

            // over open-item remaining
            expectSqlState(CHECK_VIOLATION, () -> insertArAllocation(c, clientId, receiptId, itemA, "70", "EGP"));
            // cross-client
            expectSqlState(CHECK_VIOLATION, () -> insertArAllocation(c, clientId, receiptId, foreignItem, "10", "EGP"));
            // currency mismatch
            expectSqlState(CHECK_VIOLATION, () -> insertArAllocation(c, clientId, receiptId, itemA, "10", "USD"));

            // valid: 60 to A, then receipt cap blocks 50 more (60 + 50 > 100)
            insertArAllocation(c, clientId, receiptId, itemA, "60", "EGP");
            settleAr(c, itemA, "60", "60", "0", "SETTLED");
            expectSqlState(CHECK_VIOLATION, () -> insertArAllocation(c, clientId, receiptId, itemB, "50", "EGP"));
            // 40 more is exactly the receipt cap -> allowed
            insertArAllocation(c, clientId, receiptId, itemB, "40", "EGP");
        }
    }

    @Test
    void allocationIsAppendOnlyWithExactReversalMirrors() throws Exception {
        try (Connection c = connect()) {
            int clientId = insertClient(c);
            int receiptId = insertReceipt(c, clientId, "50");
            long item = insertArOpenItem(c, clientId, "50");
            long alloc = insertArAllocation(c, clientId, receiptId, item, "50", "EGP");
            settleAr(c, item, "50", "50", "0", "SETTLED");

            // no in-place edits, no deletes
            expectSqlState(CHECK_VIOLATION, () -> exec(c,
                    "UPDATE " + SCHEMA + ".ar_receipt_allocation SET amount = 10 WHERE allocation_id = " + alloc));
            expectSqlState(CHECK_VIOLATION, () -> exec(c,
                    "DELETE FROM " + SCHEMA + ".ar_receipt_allocation WHERE allocation_id = " + alloc));

            // reversal mirror with the wrong amount is rejected
            expectSqlState(CHECK_VIOLATION, () -> insertArReversal(c, clientId, receiptId, item, "40", alloc));
            // exact mirror is accepted, then the original may flip POSTED -> REVERSED
            insertArReversal(c, clientId, receiptId, item, "50", alloc);
            exec(c, "UPDATE " + SCHEMA + ".ar_receipt_allocation SET status = 'REVERSED' WHERE allocation_id = " + alloc);
            // service re-opens the item after reversal
            settleAr(c, item, "50", "0", "50", "OPEN");
            // double reversal is rejected (original no longer POSTED)
            expectSqlState(CHECK_VIOLATION, () -> insertArReversal(c, clientId, receiptId, item, "50", alloc));
        }
    }

