    private static List<String> columnSignatures(Connection c, String schema, String table,
                                                  List<String> includedColumns) throws SQLException {
        String sql = "SELECT column_name, data_type, udt_name, is_nullable, "
                + "COALESCE(numeric_precision::text, ''), COALESCE(numeric_scale::text, '') "
                + "FROM information_schema.columns WHERE table_schema = ? AND table_name = ? "
                + "ORDER BY ordinal_position";
        List<String> signatures = new ArrayList<>();
        try (PreparedStatement p = c.prepareStatement(sql)) {
            p.setString(1, schema);
            p.setString(2, table);
            try (ResultSet rs = p.executeQuery()) {
                while (rs.next()) {
                    String column = rs.getString(1);
                    if (includedColumns == null || includedColumns.contains(column)) {
                        signatures.add(column + "|" + rs.getString(2) + "|" + rs.getString(3) + "|"
                                + rs.getString(4) + "|" + rs.getString(5) + "|" + rs.getString(6));
                    }
                }
            }
        }
        return signatures;
    }

